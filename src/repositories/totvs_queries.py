SQL_GCCUSTO: str = """
SELECT cc.CODREDUZIDO, cc.NOME, cls.DESCRICAO
FROM GCCUSTO cc
LEFT JOIN CCLASSIFICACC AS cls ON cls.CODCLASSIFICA = cc.CODCLASSIFICA
WHERE cc.CODCOLIGADA = 1
"""

