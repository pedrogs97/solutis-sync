SQL_IPATRIMONIO: str = """
SELECT p.IDPATRIMONIO, p.DESCRICAO, g.DESCRICAO AS TIPO,
    p.ATIVO, p.DATAAQUISICAO, p.PATRIMONIO, p.QUANTIDADE, p.UNIDADE, p.OBSERVACOES,
    p.CODIGOBARRA, c.NOME AS CENTROCUSTO, p.VALORBASE, p.VRDEPACUCORRIGIDA, pc.SERIE,
    pc.IMEI, pc.IMEI02, pc.ACESSORIOS01, pc.OPERADORA01, pc.SISTEMAOPERACIONAL,
    pc.PACOTEOFFICE, pc.PADRAOEQUIP01, ga.DATAEXPIRACAO AS GARANTIA,
    pc.MANUT5 AS LINHA, f.NOMEFANTASIA as FORNECEDOR,
    p.NUMERODOCUMENTO as NOTA, d.SALDORESIDUAL as DEPRECIACAO
FROM IPATRIMONIO AS p
LEFT JOIN IGRUPOPATRIMONIO AS g  ON g.IDGRUPOPATRIMONIO = p.IDGRUPOPATRIMONIO
LEFT JOIN GCCUSTO AS c           ON P.CODCENTROCUSTO = c.CODCCUSTO
LEFT JOIN IPATRIMONIOCOMPL AS pc ON pc.IDPATRIMONIO = p.IDPATRIMONIO
LEFT JOIN IGARANTIA AS ga        ON ga.IDPATRIMONIO = p.IDPATRIMONIO
LEFT JOIN FCFO as f              ON f.CODCFO = p.CODFORNECEDOR
LEFT JOIN (
    SELECT TOP 1 IDPATRIMONIO, SALDORESIDUAL
    FROM ISALDOCALCULOPATRIMONIOMOEDA
    WHERE IDPATRIMONIO IN (
        SELECT IDPATRIMONIO FROM ISALDOCALCULOPATRIMONIOMOEDA
        WHERE RECMODIFIEDON = (
            SELECT MAX(RECMODIFIEDON) FROM ISALDOCALCULOPATRIMONIOMOEDA
        )
    )
    ORDER BY IDPATRIMONIO DESC
) as d ON d.IDPATRIMONIO = p.IDPATRIMONIO
WHERE (p.CODCOLIGADA = 1)
"""

