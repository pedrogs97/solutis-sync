# ── Employees ────────────────────────────────────────────────────────

SQL_PPESSOA: str = """
SELECT
    p.CODIGO, p.NOME, p.DTNASCIMENTO, c.DESCRICAO AS CIVIL, s.DESCRICAO AS SEXO,
    n.DESCRICAO AS NACIONALIDADE, p.RUA, p.NUMERO, p.COMPLEMENTO, p.BAIRRO,
    p.ESTADO, p.CIDADE, p.CEP, p.PAIS, p.CPF, p.TELEFONE1, p.CARTIDENTIDADE,
    p.UFCARTIDENT, p.ORGEMISSORIDENT, p.DTEMISSAOIDENT, p.EMAIL, pf.NOME AS CARGO,
    cs.DESCRICAO as SITUACAO, f.DATAADMISSAO AS ADMISSAO, f.CHAPA AS MATRICULA,
    i.DESCRICAO as ESCOLARIDADE
FROM PPESSOA as p
LEFT JOIN PCODESTCIVIL AS c   ON p.ESTADOCIVIL = c.CODINTERNO
LEFT JOIN PCODSEXO AS s       ON p.SEXO = s.CODINTERNO
LEFT JOIN PCODNACAO n          ON n.CODINTERNO = p.NACIONALIDADE
LEFT JOIN PFUNC as f           ON f.CODPESSOA = p.CODIGO
LEFT JOIN PFUNCAO as pf        ON f.CODFUNCAO = pf.CODIGO
LEFT JOIN PCODSITUACAO as cs   ON cs.CODCLIENTE = f.CODSITUACAO
LEFT JOIN PCODINSTRUCAO as i   ON i.CODINTERNO = p.GRAUINSTRUCAO
"""

